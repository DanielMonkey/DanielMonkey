#include <iostream>
using namespace std;

int main()
{
   //numbers definition
   short s;
   int i;
   long l;
   float f;
   double d;

   //numbers value
   s = 10;
   i = 1000;
   l = 1000000;
   f = 230.47;
   d = 30949.374;

   //numbers output 
   cout << "short         s: " << s << endl;
   cout << "int           i: " << i << endl;
   cout << "long          l: " << l << endl;
   cout << "float         f: " << f << endl;
   cout << "double        d: " << d << endl;

   return 0;
}
